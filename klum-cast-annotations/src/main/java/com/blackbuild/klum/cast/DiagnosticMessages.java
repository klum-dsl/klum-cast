package com.blackbuild.klum.cast;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Declares message-template overrides supplied by a validation annotation.
 *
 * <p>When validation annotations are composed, the override on the nearest applicable annotation wins. A code must be
 * declared by the selected check, and every template argument must be declared for that code; otherwise validation
 * configuration fails technically rather than producing an annotation-use diagnostic.</p>
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.ANNOTATION_TYPE)
public @interface DiagnosticMessages {
    /** @return the code-keyed overrides supplied by this validation annotation */
    DiagnosticMessage[] value();
}
